class Origin
{
	private String text;
	private ArrayList<String> answers;
	
	public void setText(String t)
	{
		text = t;
	}
	
	public void setAnswers(ArrayList<String> a)
	{
		answers = a;
	}
	
	public String getText()
	{
		return text;
	}
	
	public ArrayList<String> getAnswers()
	{
		return answers;
	}
}